// Add enhanced cursor trail effect
let mouseX = 0;
let mouseY = 0;
let trail = [];

document.addEventListener('mousemove', (e) => {
    mouseX = e.clientX;
    mouseY = e.clientY;
});

function createTrail() {
    const trailElement = document.createElement('div');
    trailElement.className = 'cursor-trail';
    trailElement.style.cssText = `
        position: fixed;
        width: 6px;
        height: 6px;
        background: linear-gradient(45deg, #667eea, #764ba2);
        border-radius: 50%;
        pointer-events: none;
        z-index: 9999;
        opacity: 0.7;
        transition: all 0.1s ease;
    `;
    
    trailElement.style.left = mouseX + 'px';
    trailElement.style.top = mouseY + 'px';
    
    document.body.appendChild(trailElement);
    trail.push(trailElement);
    
    if (trail.length > 10) {
        const oldTrail = trail.shift();
        if (document.body.contains(oldTrail)) {
            document.body.removeChild(oldTrail);
        }
    }
    
    // Fade out trail elements
    trail.forEach((element, index) => {
        element.style.opacity = (index / trail.length) * 0.7;
        element.style.transform = `scale(${(index / trail.length) * 0.5 + 0.5})`;
    });
}

// Create trail effect
setInterval(createTrail, 50);

